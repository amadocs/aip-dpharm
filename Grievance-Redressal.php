Grievances from students are put first in front of the Mentor allotted to that batch of students. Mentor along with class teacher of respective class consider the grievances and put forth for discussion. After discussion of the grievances in the meeting Class teachers and Class representative, the report is submitted to the Principal and subsequently to the management for the taking further necessary action in that matter. The management takes such action as it deems fit and the decision of the management are final. Student have given facility of online grievance at the college website (www.adarshpharmacy.edu.in).
Grievances of staff member are considered in the same way in the meeting of HOD & Director.<br/>

